package com.trini.whoiswho;

/**
 *
 * @author asura
 */
public class Puntuacion {

    //poner nombre y puntos
    private String nombre;
    private int puntos;

    Puntuacion(String nombre,
            int puntos) {
        this.nombre = nombre;
        this.puntos = puntos;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getPuntos() {
        return puntos;
    }

    public void setPuntos(int puntos) {
        this.puntos = puntos;
    }
    
}
